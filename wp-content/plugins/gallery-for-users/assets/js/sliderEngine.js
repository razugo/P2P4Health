jQuery(window).load(function($){
    /**
     * show slider date on slide change
     */
    jQuery("#amazingslider-12").bind("amazingslider.switch", function(event, previous, next){ 
        var current_slide_key = next+1;
        var new_date = jQuery('.amazingslider-slides').children('li:nth-child('+current_slide_key+')').data('usr-date');
        var new_url  = jQuery('.amazingslider-slides').children('li:nth-child('+current_slide_key+')').data('usr-url');
        jQuery('.fb-share').attr('data-href', new_url);
        jQuery('.tweet_share').attr('data-href', new_url);
        jQuery('.gplus_share').attr('data-href', new_url);
        jQuery('.linkedin_share').attr('data-href', new_url);
        //Update upload date on DOM
        jQuery('#upload_date').html(new_date);
    });
});